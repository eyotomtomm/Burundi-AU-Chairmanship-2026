/// App-wide spacing scale.
///
/// Use these constants instead of ad-hoc numeric literals so that padding,
/// margins, and gaps stay consistent across the entire UI.
class AppSpacing {
  // Core scale (multiples of 4)
  static const double xs = 4;
  static const double sm = 8;
  static const double md = 12;
  static const double lg = 16;
  static const double xl = 24;
  static const double xxl = 32;

  // Semantic aliases
  static const double pagePadding = 16;
  static const double cardRadius = 12;
  static const double cardRadiusLg = 16;
}
