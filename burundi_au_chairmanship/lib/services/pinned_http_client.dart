  static http.Client? _cachedClient;

  /// Create an HTTP client with certificate pinning for production,
  /// or a standard client for development.
  static http.Client create() {
    if (_cachedClient != null) return _cachedClient!;

    if (kDebugMode) {
      // Development: no pinning (need localhost access)
      _cachedClient = http.Client();
      return _cachedClient!;
    }

    try {
      // Production: pin to specific CA certificates only
      final context = SecurityContext(withTrustedRoots: true);
      context.setTrustedCertificatesBytes(
        _pinnedCertificates.codeUnits,
      );

      final httpClient = HttpClient(context: context);
      // Reject any certificate that doesn't chain to our pinned CAs
      httpClient.badCertificateCallback = (cert, host, port) {
        // In production, never accept bad certificates
        return false;
      };

      _cachedClient = IOClient(httpClient);
      return _cachedClient!;
    } catch (e) {
      // If pinning setup fails, fall back to system roots
      // (better than bricking the app)
      if (kDebugMode) print('Certificate pinning setup failed: $e');
      _cachedClient = http.Client();
      return _cachedClient!;
    }
  }
